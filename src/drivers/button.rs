pub fn init() {
	gpio::pin_setup(config::PUSH_BUTTON_IN_PIN, DIR::INPUT, gpio::PinState::PinLow, PULL::PULLUP);
	gpio::pin_setup(config::PUSH_BUTTON_OUT_PIN, DIR::OUTPUT, gpio::PinState::PinLow, PULL::DISABLED);
}

