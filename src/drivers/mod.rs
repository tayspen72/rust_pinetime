#[allow(dead_code)]
pub enum DriversState{
	BusyLcd,
	
	Idle,
}

//==============================================================================
// Public Functions
//==============================================================================
pub fn get_busy() -> DriversState {
    if lcd::lcd_api::get_busy() {
        return DriversState::BusyLcd;
    }

    DriversState::Idle
}

