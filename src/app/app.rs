pub fn get_state() -> super::info::AppState {
    if lcd::lcd_api::get_busy() {
        return super::info::AppState::BusyLcd;
    }
    else if timer::get_busy() {
        return super::info::AppState::BusyTimer;
    }

    super::info::AppState::Idle
}

