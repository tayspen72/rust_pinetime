//==============================================================================
// Task Handler
//==============================================================================
pub fn task_handler(d: &mut info::DeviceInfo) {
	if d.change_flags.button_press {
		();
	}
	
	if d.change_flags.time_change {
		clock::update_time();
	}

	if d.change_flags.touch_event {
		();
	}
}