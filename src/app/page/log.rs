//==============================================================================
// Task Handler
//==============================================================================
#[allow(dead_code)]
pub fn task_handler(d: &mut info::DeviceInfo) {
	if d.change_flags.touch_event {
		if let Gesture::SlideDown = d.touch.gesture {
			d.app_page = page::AppPage::Home;
			page::change_page(d);
			return;
		}
	}

	if d.change_flags.button_press {
		if !d.flags.button_pressed {
			d.app_page = page::AppPage::Home;
			page::change_page(d);
			return;
		}
	}
}