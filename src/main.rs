#[entry]
fn main() -> ! {
	let peripherals = nrf52832_pac::Peripherals::take().unwrap();

	app_init(&peripherals);
	
	loop {
		app_task_handler(&peripherals);
	};
}

fn app_init(p: &nrf52832_pac::Peripherals) {
	debug::init(p);
	button::init(p);
	touch::init(p);
	lcd::lcd_api::init(p);
}

