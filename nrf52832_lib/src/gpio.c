uint8_t Gpio_GetPinState(uint8_t pin){
	NRF_P0->IN & (1 << pin) ? 1 : 0;
}

void GPIO_PinSetup(uint8_t pin, GpioPinDirection_t direction, uint8_t state, uint8_t pullUpDown){
	if(direction == INPUT)
	{
		if(pullUpDown)
			NRF_P0->PIN_CNF[pin] = 0xC;
		else
			NRF_P0->PIN_CNF[pin] = 0x4;
	}
	else
	{
		NRF_P0->PIN_CNF[pin] = 0x1;

		if(state == true)
			NRF_P0->OUTSET = (1 << pin);
		else
		{
			uint32_t clr = 1 << pin;

			NRF_P0->OUTCLR = clr;
		}
	}
}

void GPIO_SetPinState(uint8_t pin, uint8_t state){
	// Set pin state
	if(state)
		NRF_P0->OUTSET = (1 << pin);
	else
		NRF_P0->OUTCLR = (1 << pin);
}

